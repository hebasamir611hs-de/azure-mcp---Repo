def create_revised_uat_document(
    original_uat_path: str,
    review_comments: list,
    additional_test_cases: list = None,
    output_path: str = ""
) -> dict:
    """
    SKILL 11b: UAT Polishing & Revision

    Applies QA Manager review comments and appends missing test cases
    to an existing UAT document. Adds a bilingual review notes section.

    Each item in additional_test_cases: {title, steps: [{action, expected}], language?: "ar"|"en"}
    Language detection per TC mirrors Skills 3/4/10 — uses "language" field or auto-detects from title.

    Args:
        original_uat_path (str): Path to original UAT .docx
        review_comments (list[str]): QA Manager feedback lines
        additional_test_cases (list[dict]): New TCs to append
        output_path (str): Output path (defaults to original_Reviewed.docx)

    Returns:
        {status, file_path, language, review_comments_count, additional_test_cases_added}
    """
    if additional_test_cases is None:
        additional_test_cases = []

    try:
        if not os.path.exists(original_uat_path):
            return {"error": f"File not found: {original_uat_path}"}

        doc = DocxDocument(original_uat_path)
        all_text = "\n".join(p.text for p in doc.paragraphs if p.text)
        doc_is_ar = is_arabic(all_text)
        today = datetime.now().strftime("%Y-%m-%d")
        added_tc_count = 0

        for tc_data in additional_test_cases:
            tc_title = tc_data.get('title', '')
            tc_steps = tc_data.get('steps', [])
            tc_lang = tc_data.get('language', 'ar' if is_arabic(tc_title) else 'en')

            tc_dict = {"title": tc_title, "steps": tc_steps}

            if tc_lang == "ar":
                _add_arabic_tc_table(doc, tc_dict)
            else:
                _add_english_tc_table(doc, tc_dict, "")

            added_tc_count += 1
            doc.add_paragraph("")

        doc.add_page_break()
        review_title = "ملاحظات المراجعة" if doc_is_ar else "Review Notes"
        doc.add_heading(review_title, level=1)
        doc.add_paragraph(f"{'تمت المراجعة بواسطة' if doc_is_ar else 'Reviewed by'}: QA Manager (AI-Assisted Review)")
        doc.add_paragraph(f"{'تاريخ المراجعة' if doc_is_ar else 'Review Date'}: {today}")
        doc.add_heading("التغييرات المطبقة" if doc_is_ar else "Changes Applied", level=2)
        for i, comment in enumerate(review_comments, 1):
            doc.add_paragraph(f"{i}. {comment}")

        if not output_path:
            base, ext = os.path.splitext(original_uat_path)
            output_path = f"{base}_Reviewed{ext}"

        doc.save(output_path)
        return {
            "status": "success",
            "file_path": os.path.abspath(output_path),
            "language": "ar" if doc_is_ar else "en",
            "review_comments_count": len(review_comments),
            "additional_test_cases_added": added_tc_count
        }

    except Exception as e:
        return handle_error(e, "create_revised_uat_document")
