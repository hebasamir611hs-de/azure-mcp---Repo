def _add_arabic_tc_table(doc, tc: dict):
    """3-column RTL table for Arabic TCs: Expected | Action | Step."""
    num_steps = len(tc['steps'])
    tbl = doc.add_table(rows=2 + max(num_steps, 1), cols=3, style="Table Grid")
    merge_cells_in_row(tbl, 0, 0, 2)

    title_cell = tbl.cell(0, 0)
    title_cell.text = tc['title']
    set_cell_shading(title_cell, "FDE9D9")
    for p in title_cell.paragraphs:
        for r in p.runs:
            r.font.bold = True
            r.font.size = Pt(12)
            r.font.color.rgb = RGBColor(0x1F, 0x38, 0x64)
        apply_rtl(p)

    for j, h in enumerate(["النتيجة المتوقعة", "الإجراء", "الخطوة"]):
        cell = tbl.cell(1, j)
        cell.text = h
        set_cell_shading(cell, "D9D9D9")
        for p in cell.paragraphs:
            for r in p.runs:
                r.font.bold = True
            apply_rtl(p)

    if tc['steps']:
        for s_idx, step in enumerate(tc['steps']):
            tbl.cell(2 + s_idx, 0).text = step['expected']
            tbl.cell(2 + s_idx, 1).text = step['action']
            tbl.cell(2 + s_idx, 2).text = str(s_idx + 1)
            for j in range(3):
                for p in tbl.cell(2 + s_idx, j).paragraphs:
                    apply_rtl(p)
    else:
        tbl.cell(2, 2).text = "1"

    doc.add_paragraph("")


def _add_english_tc_table(doc, tc: dict, ac_text: str):
    """4-column LTR table for English TCs with prerequisites row."""
    ac_clean = re.sub(r'\s+', ' ', re.sub(r'<[^>]+>', ' ', ac_text)).strip()
    num_steps = len(tc['steps'])
    tbl = doc.add_table(rows=3 + max(num_steps, 1), cols=4, style="Table Grid")

    merge_cells_in_row(tbl, 0, 0, 3)
    title_cell = tbl.cell(0, 0)
    title_cell.text = f"TC : {tc['title']}"
    set_cell_shading(title_cell, "D9E2F3")
    for p in title_cell.paragraphs:
        for r in p.runs:
            r.font.bold = True
            r.font.size = Pt(10)

    merge_cells_in_row(tbl, 1, 0, 3)
    prereq_cell = tbl.cell(1, 0)
    prereq_cell.text = f"Prerequisites: {ac_clean[:200]}"
    for p in prereq_cell.paragraphs:
        for r in p.runs:
            r.font.size = Pt(9)
            r.font.italic = True

    for j, h in enumerate(["Step", "Action", "Expected System Response", "Feedback"]):
        cell = tbl.cell(2, j)
        cell.text = h
        set_cell_shading(cell, "E2EFDA")
        for p in cell.paragraphs:
            for r in p.runs:
                r.font.bold = True
                r.font.size = Pt(9)

    if tc['steps']:
        for s_idx, step in enumerate(tc['steps']):
            tbl.cell(3 + s_idx, 0).text = str(s_idx + 1)
            tbl.cell(3 + s_idx, 1).text = step['action']
            tbl.cell(3 + s_idx, 2).text = step['expected']
            tbl.cell(3 + s_idx, 3).text = ""
            for j in range(4):
                for p in tbl.cell(3 + s_idx, j).paragraphs:
                    for r in p.runs:
                        r.font.size = Pt(9)
    else:
        tbl.cell(3, 0).text = "1"

    doc.add_paragraph("")


def generate_uat_document(
    iteration_path: str,
    project_name: str = "",
    output_path: str = "",
    include_all: bool = False
) -> dict:
    """
    SKILL 10: Client-Ready UAT Generation

    Generates a professional UAT (.docx) from a sprint, auto-detecting language
    per PBI. English PBIs use 4-column LTR tables with a cover page and changelog.
    Arabic PBIs use 3-column RTL tables.

    TC Filter (default): Only Functional + Positive test cases (stakeholder UAT scope).
    Set include_all=True to include Edge, Negative, and Intensive cases.

    Args:
        iteration_path (str): Sprint path
        project_name (str): Optional project name for cover page
        output_path (str): Optional output file path
        include_all (bool): Include all TC types (default: False)

    Returns:
        {status, file_path, language, total_pbis, total_test_cases, pbi_summary}
    """
    try:
        client = get_azure_client()
        project = os.getenv("AZURE_PROJECT")
        if not project_name:
            project_name = project
        safe_iter = sanitize_wiql_string(iteration_path)
        safe_proj = sanitize_wiql_string(project)

        query = f"""
            SELECT [System.Id], [System.Title], [Microsoft.VSTS.Common.AcceptanceCriteria]
            FROM WorkItems
            WHERE [System.WorkItemType] = 'Product Backlog Item'
              AND [System.IterationPath] = '{safe_iter}'
              AND [System.TeamProject] = '{safe_proj}'
            ORDER BY [System.Id] ASC
        """
        result = client.query_by_wiql(Wiql(query=query))

        if not result.work_items:
            return {"error": "No PBIs found in this sprint.", "iteration_path": iteration_path}

        pbi_ids = [item.id for item in result.work_items]
        pbi_items = client.get_work_items(ids=pbi_ids, expand="All")

        all_pbi_data = []
        ar_count = en_count = 0

        for pbi in pbi_items:
            pbi_title = pbi.fields.get('System.Title', '')
            pbi_ac = pbi.fields.get('Microsoft.VSTS.Common.AcceptanceCriteria', 'No AC')
            pbi_lang = "ar" if (is_arabic(pbi_title) or is_arabic(pbi_ac)) else "en"

            if pbi_lang == "ar":
                ar_count += 1
            else:
                en_count += 1

            tc_ids = [
                int(rel.url.split("/")[-1])
                for rel in (pbi.relations or [])
                if rel.rel == "System.LinkTypes.Hierarchy-Forward"
            ]

            test_cases = []
            if tc_ids:
                tc_items = client.get_work_items(ids=tc_ids, expand="All")
                for tc in tc_items:
                    if tc.fields.get('System.WorkItemType') != 'Test Case':
                        continue

                    tc_title = tc.fields.get('System.Title', '')
                    tc_tags = tc.fields.get('System.Tags', '')
                    tag_list = [t.strip() for t in tc_tags.split(';')] if tc_tags else []

                    if not include_all:
                        has_tags = any(t in tag_list for t in ["UI", "Functional", "Edge", "Intensive", "positive", "negative"])
                        tc_type = next((t for t in tag_list if t in ["UI", "Functional", "Edge", "Intensive"]), None) if has_tags else None
                        tc_scenario = next((t for t in tag_list if t in ["positive", "negative"]), None) if has_tags else None

                        if not has_tags:
                            inferred = infer_tc_attributes_from_title(tc_title)
                            tc_type = inferred["test_type"]
                            tc_scenario = inferred["scenario"]

                        if tc_type != "Functional" or tc_scenario != "positive":
                            continue

                    test_cases.append({
                        "id": tc.id,
                        "title": tc_title,
                        "priority": tc.fields.get('Microsoft.VSTS.Common.Priority', 2),
                        "steps": parse_steps_xml(tc.fields.get('Microsoft.VSTS.TCM.Steps', ''))
                    })

            all_pbi_data.append({
                "id": pbi.id,
                "title": pbi_title,
                "ac": pbi_ac,
                "language": pbi_lang,
                "test_cases": test_cases
            })

        majority_is_ar = ar_count >= en_count
        doc = DocxDocument()
        today = datetime.now().strftime("%Y-%m-%d")
        today_full = datetime.now().strftime("%b %dth, %Y")
        sprint_label = iteration_path.split("\\")[-1] if "\\" in iteration_path else iteration_path
        total_tcs = 0

        # Document header
        if majority_is_ar:
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run(f"حالات الاختبار - {project_name}")
            run.bold = True
            run.font.size = Pt(16)
            apply_rtl(p)
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run(f"{project_name} — {sprint_label} Test Cases")
            run.font.size = Pt(12)
            apply_rtl(p)
            doc.add_paragraph("")
            doc.add_page_break()
            p = doc.add_heading("نطاق العمل (عناصر قائمة المنتج)", level=1)
            apply_rtl(p)
            for p_data in all_pbi_data:
                if p_data['test_cases']:
                    p = doc.add_paragraph(f"• PBI {p_data['id']}: {p_data['title']}")
                    apply_rtl(p)
            doc.add_page_break()
            p = doc.add_heading("حالات الاختبار (Test Cases)", level=1)
            apply_rtl(p)
        else:
            doc.add_paragraph("")
            doc.add_paragraph("")
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run("Prepared For")
            run.font.size = Pt(14)
            run.font.color.rgb = RGBColor(0x80, 0x80, 0x80)
            doc.add_paragraph("")
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run(project_name)
            run.bold = True
            run.font.size = Pt(28)
            run.font.color.rgb = RGBColor(0x2E, 0x50, 0x90)
            doc.add_paragraph("")
            doc.add_paragraph("")
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run("By")
            run.font.size = Pt(14)
            run.font.color.rgb = RGBColor(0x80, 0x80, 0x80)
            doc.add_paragraph("")
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run("AI-Assisted QA (Claude)")
            run.bold = True
            run.font.size = Pt(16)
            doc.add_paragraph("")
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run(today_full)
            run.font.size = Pt(14)
            doc.add_page_break()
            doc.add_heading("Document change Log", level=1)
            log_table = doc.add_table(rows=2, cols=4)
            for j, h in enumerate(["Date", "Changed By", "Reason Of Change", "Description of Change"]):
                cell = log_table.cell(0, j)
                cell.text = h
                set_cell_shading(cell, "D5E8F0")
                for p in cell.paragraphs:
                    for r in p.runs:
                        r.font.bold = True
                        r.font.size = Pt(10)
            log_table.cell(1, 0).text = today
            log_table.cell(1, 1).text = "AI-Assisted QA (Claude)"
            log_table.cell(1, 2).text = "Document Creation"
            log_table.cell(1, 3).text = ""
            doc.add_paragraph("")
            doc.add_heading("Introduction", level=1)
            doc.add_paragraph(f"This document represents {project_name} acceptance Checklist for {project_name} Site, and all its functional specifications.")
            doc.add_heading("Pre-Requisite Checklist(s)", level=1)
            doc.add_paragraph("It's recommended that the user is already acquainted with the system.")
            doc.add_page_break()
            doc.add_heading("Scope of Work (Product Backlog Items)", level=1)
            for p_data in all_pbi_data:
                if p_data['test_cases']:
                    doc.add_paragraph(f"• PBI {p_data['id']}: {p_data['title']}", style='List Bullet')
            doc.add_page_break()
            doc.add_heading("Test Cases", level=1)

        # Per-PBI sections
        for pbi_data in all_pbi_data:
            if not pbi_data['test_cases']:
                continue

            pbi_is_ar = pbi_data['language'] == "ar"
            p = doc.add_paragraph()
            run = p.add_run(pbi_data['title'])
            run.bold = True
            run.font.size = Pt(12)
            run.font.color.rgb = RGBColor(0x2E, 0x50, 0x90)
            if pbi_is_ar:
                apply_rtl(p)

            for tc in pbi_data['test_cases']:
                total_tcs += 1
                if pbi_is_ar:
                    _add_arabic_tc_table(doc, tc)
                else:
                    _add_english_tc_table(doc, tc, pbi_data['ac'])

        if not output_path:
            safe_name = project_name.replace(" ", "_")
            safe_sprint = sprint_label.replace(" ", "_")
            output_path = f"UAT_Document_{safe_name}_{safe_sprint}_{datetime.now().strftime('%Y-%m-%d')}.docx"

        doc.save(output_path)
        return {
            "status": "success",
            "file_path": os.path.abspath(output_path),
            "language": "ar" if majority_is_ar else "en",
            "total_pbis": len(all_pbi_data),
            "total_test_cases": total_tcs,
            "iteration_path": iteration_path,
            "pbi_summary": [
                {"id": p["id"], "title": p["title"], "test_cases_count": len(p["test_cases"])}
                for p in all_pbi_data
            ]
        }

    except Exception as e:
        return handle_error(e, "generate_uat_document")


