def generate_playwright_yaml(parent_id: int) -> dict:
    """
    SKILL 8: Smart YAML Automation

    Generates Playwright-ready YAML for all Automated TCs under a PBI.
    Manual TCs are excluded to prevent framework contamination.

    Args:
        parent_id (int): The PBI/User Story work item ID

    Returns:
        {parent_id, parent_title, test_cases_count, automated_count,
         manual_skipped, yaml_content}
    """
    try:
        client = get_azure_client()

        parent = client.get_work_item(parent_id, expand="Relations")
        parent_title = parent.fields.get('System.Title', '')

        if not parent.relations:
            return {"error": "No test cases linked to this PBI."}

        tc_ids = [
            int(rel.url.split("/")[-1])
            for rel in parent.relations
            if rel.rel == "System.LinkTypes.Hierarchy-Forward"
        ]

        if not tc_ids:
            return {"error": "No child test cases found."}

        work_items = client.get_work_items(ids=tc_ids, expand="All")

        yaml_lines = [
            "# Auto-generated Playwright test suite",
            f"# Source PBI: {parent_id} - {parent_title}",
            "# Generated by: QA-Final-V4 MCP Server",
            "# Note: Only 'Automated' test cases included",
            "",
            "suite:",
            f'  name: "PBI-{parent_id}"',
            f'  description: "{parent_title}"',
            "  tests:"
        ]

        automated_count = manual_count = total_count = 0

        for item in work_items:
            if item.fields.get('System.WorkItemType') != 'Test Case':
                continue

            total_count += 1
            tc_title = item.fields.get('System.Title', '')
            tags = item.fields.get('System.Tags', '')
            tag_list = [t.strip() for t in tags.split(';')] if tags else []

            if "Manual" in tag_list:
                manual_count += 1
                continue

            automated_count += 1

            test_type = next((t.lower() for t in tag_list if t in ["UI", "Functional", "Edge", "Intensive"]), "functional")
            scenario = next((t for t in tag_list if t in ["positive", "negative"]), "positive")
            impact_area = next((t for t in tag_list if t in ["UI", "Backend", "Both"]), "UI")

            steps_raw = parse_steps_xml(item.fields.get('Microsoft.VSTS.TCM.Steps', ''))
            steps = [s["action"] for s in steps_raw]
            expected = [s["expected"] for s in steps_raw]

            yaml_lines += [
                "",
                f'    - id: "TC-{item.id}"',
                f'      title: "{tc_title}"',
                f'      type: "{test_type}"',
                f'      scenario: "{scenario}"',
                f'      impact_area: "{impact_area}"',
                f'      execution_type: "Automated"',
                f'      tags: {tag_list}',
                "      steps:"
            ]

            for j, (s, e) in enumerate(zip(steps, expected), 1):
                yaml_lines += [
                    f'        - step: {j}',
                    f'          action: "{s}"',
                    f'          expected: "{e}"'
                ]

        return {
            "parent_id": parent_id,
            "parent_title": parent_title,
            "test_cases_count": total_count,
            "automated_count": automated_count,
            "manual_skipped": manual_count,
            "yaml_content": "\n".join(yaml_lines)
        }

    except Exception as e:
        return handle_error(e, "generate_playwright_yaml")


